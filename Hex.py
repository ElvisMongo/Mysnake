def symbols(x): # данная функция сопоставляет числам 10, 11, 12, 13, 14 и 15 соответствующий буквенный эквивалент в шестнадцатеричной системе исчисления
	if x == 10:
		return 'A'
				
	if x == 11:
		return 'B'
				
	if x == 12:
		return 'C'
				
	if x == 13:
		return 'D'
				
	if x == 14:
		return 'E'
				
	if x == 15:
		return 'F'
				
	else:
		return str(x)

